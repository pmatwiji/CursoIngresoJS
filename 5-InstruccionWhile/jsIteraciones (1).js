function mostrar()
{
	//alert('iteración while');
	var contador = 1;

	while (contador <= 10) {

		console.log(contador);
		
		switch (contador){
			case 2:
				console.log("Freddy`s coming for you");
				break;
			case 4:
				console.log("Shut the door");
				break;
			case 6:
				console.log("Take the crucifix");
				break;
			case 8:
				console.log("Gonna stay up late");
				break;
			case 10:
				console.log("Never sleep again");
		}


		contador ++;


	}

}//FIN DE LA FUNCIÓN